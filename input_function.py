# INPUT FUNCTION
# Input is a function which asks the user for a value and 
# gives you back the value that the user has provided.
# Input function always returns a string value.

print('What is your name?')
user_name = input()
print('Hello,', user_name)

# Result: Python asks your name (Input). 
# You enter your name and Python returns
# "Hello, Input"

# Functions in Python can:
# 1. Cause some effect
# 2. Return a value as the function's result

# Input function causes some effect because it asks the user 
# to provide a value in the console and it returns to us 
# whatever the user provided.

user_name = input ('What is your name?')
print('Hello,',user_name)




